# Taking input from the user using input()
name = input("What is your name: ")  # Prompting user for their name and storing it in 'name' variable
age = input("What is your age: ")  # Prompting user for their age and storing it in 'age' variable

# Printing out the user's name and age
print("Hello", name)  # Printing a greeting followed by the user's name
print("You are", age, "years old")  # Printing a statement about the user's age
