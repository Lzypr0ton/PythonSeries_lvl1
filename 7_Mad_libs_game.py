# Taking input from the user
colour = input("Enter a color: ")  # Asking user to input a color
plural_noun = input("Enter a plural noun: ")  # Asking user to input a plural noun
celebrity = input("Enter a celebrity: ")  # Asking user to input a celebrity

# Printing the Mad Libs story with user inputs
print("Roses are " + colour)  # Combining the color input with the string "Roses are"
print(plural_noun + " are blue")  # Combining the plural noun input with the string " are blue"
print("I love " + celebrity)  # Combining the celebrity input with the string "I love"
