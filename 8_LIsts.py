# List is a collection of data which is in sequence and changeable
friends = ["Tony", "Chris", "Sachin", "May"]

# Print the entire list
print(friends)

# Print a specific element in the list (indexing starts from 0)
print(friends[0])  # prints specific data

# Change an element in the list
friends[0] = "Salman"  # changing the data

# Print the new element at index 0
print(friends[0])  # new data printing

# Conclusion about lists in Python:
# NOTE: Lists are mutable
